package Gun03;

public class _01_Degiskenler {
    public static void main(String[] args) {
       int sayi; // int, integer yani tam sayı demek

        sayi=5;  // sayıya 5 değerini verdim.
        System.out.println("sayi"); // sayı kelimesini yazar.
        System.out.println(sayi); // tırnaksız olduğunda değerini yazar, yani "5" yazar.

        System.out.println("sayi="+sayi); // sayı= sabit yazısının yanına sayının değerini yazar.
                                          // bu islemin kısaltması "sout" yerine "soutv" dir.

        /* sayi
           5
           sayi=5 */


    }
}
