package Gun02;

public class _03_Ornek2 {
    public static void main(String[] args) {
        System.out.println("Merhaba Dünya");
        // ben ekrana yazdırırım, "çift tırnak" arasındaki her şeyi aynen yazdırırım
        // "noktalı virgül" emrim bitti yerine getir.
        // kod adında ki büyük-küçün harfin farkı vardır. A-a yı A-Z gibi görür.

        System.out.println("Merhaba Dünya 2"); //ln (line) var ise yazdıktan sonra alt satıra geçer

        System.out.print("merhaba 1");  // 2 print kodu yan yana yazar boşluk vermez,
                                        // println gibi yeni satır açmaz
        System.out.println(); // bir şey yazmaz sadece yeni satır açar
        System.out.print("merhaba 2");
        System.out.println();
        System.out.println("Merhaba \n Dünya ");  // \n ---> yeni satır açar

    }
}
