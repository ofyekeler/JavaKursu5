package Mentoring.mentoring_1;

public class DataTypes {
    public static void main(String[] args) {


        byte b = 127;  // -128 ..  127   değer aralığı
        short s = 23436;  // -32768  .. 32767
        int i = 14789;  // 2147483648  ..2147483647
        long l = 123654147;   //9223372036854775808  ... 9223372036854775807
        float f = 3.45f;  // noktadan sonra 7 hane duyarlıdır
        double d = 6788888.23;  //noktadan son 16 hane duyarlıdır
        //*****************************************

        char x = 'A';
        char y = 'B';

        int z = x * y;   // ASCII tablosu değerleriyle çarpma işlemi yapıdı.
        System.out.println("A ve B çarpımı sonucu = " + z);






    }
}
